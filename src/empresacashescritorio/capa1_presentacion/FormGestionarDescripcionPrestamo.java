    private DescripcionPrestamo buscarDescripcionPrestamo(int codigo) {
        DescripcionPrestamo descripcionPrestamo = null;
        for (DescripcionPrestamo cliente : listDescripcionPrestamo) {
            if (cliente.getId() == codigo) {
                descripcionPrestamo = cliente;
            }
        }
        return descripcionPrestamo;
    }

