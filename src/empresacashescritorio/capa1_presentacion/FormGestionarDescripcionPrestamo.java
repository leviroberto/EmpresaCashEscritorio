    private void btn_ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ModificarActionPerformed
        try {
            DescripcionPrestamo descripcionPrestamo = buscarDescripcionPrestamo((int) tabla_DatosAFP.getValueAt(filaSeleccionada, 0));
            VentanaDescripcionPrestamoDatos ventanaDatosDescripcionPrestamo = new VentanaDescripcionPrestamoDatos(null, true, descripcionPrestamo);
            ventanaDatosDescripcionPrestamo.setVisible(true);
            llenarDatosTabla();
        } catch (Exception e) {
            System.out.println("sad" + e.getMessage());
        }
    }//GEN-LAST:event_btn_ModificarActionPerformed

    private void btn_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EliminarActionPerformed

        if (filaSeleccionada != -1) {
            int codigo = (int) tabla_DatosAFP.getValueAt(filaSeleccionada, 0);
            int indice = JOptionPane.showConfirmDialog(this, "Desea eliminar", "FiveCod: Pregunta", JOptionPane.YES_NO_OPTION);
            if (indice == 1) {
                return;
            }
            try {
                int registros_afectados = gestionarDescripcionPrestamoServicio.eliminar(codigo);
                if (registros_afectados == 1) {
                    Mensaje.mostrarAfirmacionDeEliminacion();
                    llenarDatosTabla();
                } else {
                    Mensaje.mostrarErrorDeEliminacion();
                }
                
            } catch (Exception e) {
                Mensaje.mostrarAdvertenciaDeEliminacion();
            }
            
        } else {
            Mensaje.mostrarFilaNoSeleccionada();
        }
    }//GEN-LAST:event_btn_EliminarActionPerformed

