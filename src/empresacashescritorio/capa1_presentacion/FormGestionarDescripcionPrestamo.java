    private void inicializarTablaColumnas() {
        Tabla tabla = new Tabla();
        tabla.agregarColumna(new Columna("Codigo", "java.lang.Integer"));
        tabla.agregarColumna(new Columna("COMSIÒN", "java.lang.String"));
        tabla.agregarColumna(new Columna("MONTO", "java.lang.String"));
        tabla.agregarColumna(new Columna("MORA", "java.lang.String"));
        tabla.agregarColumna(new Columna("PUNTOS", "java.lang.String"));
        tabla.agregarColumna(new Columna("DESCRIPCIÒN", "java.lang.String"));

        tabla.agregarColumna(new Columna("ESTADO", "java.lang.String"));

        ModeloTabla modeloTabla = new ModeloTabla(tabla);
        tabla_DatosAFP.setModel(modeloTabla);
        tabla_DatosAFP.getColumn(tabla_DatosAFP.getColumnName(0)).setWidth(0);
        tabla_DatosAFP.getColumn(tabla_DatosAFP.getColumnName(0)).setMinWidth(0);
        tabla_DatosAFP.getColumn(tabla_DatosAFP.getColumnName(0)).setMaxWidth(0);
    }

    private void llenarDatosTabla() throws Exception {
        listDescripcionPrestamo = gestionarDescripcionPrestamoServicio.mostrar();
        Fila filaTabla;
        try {
            ModeloTabla modeloTabla = (ModeloTabla) tabla_DatosAFP.getModel();
            modeloTabla.eliminarTotalFilas();
            if (listDescripcionPrestamo.size() == 0) {
                Mensaje.mostrarAdvertenciaSinElementos();
            } else {
                for (DescripcionPrestamo descripcionPrestamo : listDescripcionPrestamo) {
                    filaTabla = new Fila();
                    filaTabla.agregarValorCelda(descripcionPrestamo.getId());
                    filaTabla.agregarValorCelda(descripcionPrestamo.getComision());
                    filaTabla.agregarValorCelda(descripcionPrestamo.getMonto());
                    filaTabla.agregarValorCelda(descripcionPrestamo.getMora());
                    filaTabla.agregarValorCelda(descripcionPrestamo.getPuntos());
                    filaTabla.agregarValorCelda(descripcionPrestamo.getDescripcion());
                    filaTabla.agregarValorCelda(descripcionPrestamo.getEstado());

                    modeloTabla.agregarFila(filaTabla);
                }
                modeloTabla.refrescarDatos();
            }

        } catch (Exception e) {
            Mensaje.mostrarErrorSistema();
        }

    }

